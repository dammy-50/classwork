#gatepass checker
print("welcome to our gatepass checker")
print("enter your age")
age=int(input())
checker=17
if age<checker:
    print("you are not welcome")
elif age<=checker:
    print("you are not welcome")
elif age!=checker:
    print("you are welcome")
else:
    print("you are welcome")


